'''Numbers'''

a = 1
b = 2
c = 3

li = [a,b,c]
li2 = [c,b,a]

'''Printing the full list'''
print( 'list with int values:\n',li )                  
'''Printing first value of list'''
print( li[0] )                         
'''Printing second & third values of list'''
print( li[1:2] )                       
'''Printing second to last value of list'''
print( li[1:] )                        
'''Printing last value of list'''
print( li[2] )                         
'''Printing the list twice'''
print( li * 2 )                        
'''Printing the list concatenate with an another list'''
print( li+li2 )
print( li2+li )    


